WITH RENEWEL AS (
    SELECT C.CAR_TYPE, C.DAILY_FEE, H.HISTORY_ID, DATEDIFF(H.END_DATE, H.START_DATE) + 1 AS DURATION, CASE
    WHEN DATEDIFF(H.END_DATE, H.START_DATE) +1 >= 90 THEN '90일 이상'
    WHEN DATEDIFF(H.END_DATE, H.START_DATE) +1 >= 30 THEN '30일 이상'
    WHEN DATEDIFF(H.END_DATE, H.START_DATE) +1 >= 7 THEN '7일 이상'
    ELSE 'NONE' END AS DISCOUNT_TYPE
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY H JOIN CAR_RENTAL_COMPANY_CAR C ON H.CAR_ID = C.CAR_ID
WHERE C.CAR_TYPE = '트럭'
)

SELECT R.HISTORY_ID, ROUND(R.DAILY_FEE * R.DURATION * 0.01 * (100 - IFNULL(P.DISCOUNT_RATE, 0)), 0) AS FEE
FROM RENEWEL R LEFT JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P ON R.DISCOUNT_TYPE = P.DURATION_TYPE AND R.CAR_TYPE = P.CAR_TYPE
ORDER BY 2 DESC, 1 DESC;